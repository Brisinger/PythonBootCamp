a = True
b = False

print("Logical and operator", a, "and", b)
print(a & b)
print("Logical & operator", a, "&", b)
print(a and b)

print("\nLogical or operator", a, "or", b)
print(a or b)
print("Logical | operator", a, "|", b)
print(a | b)

print("\nLogical not operator", "not(", a, ")")
print(not(a))
print("Logical not operator", "not", b)
print(not b)
